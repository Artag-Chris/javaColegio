    public void validar() {
        if (txtNombre.getText().isEmpty()) {
            txtNombre.grabFocus();
        } else if (ComboGrado.getSelectedIndex() == 0) {
            ComboGrado.grabFocus();
        }
    }
    public void capturar() {
        objMat.setCodMat(Byte.parseByte(txtDocumento.getText()));
        objMat.setNomMat(txtNombre.getText());
        objMat.setGraMat(ComboGrado.getSelectedItem().toString());
    }
